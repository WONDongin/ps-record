#include <stdio.h>
#include <string.h>
/*
문제-5622 (브론즈2): 다이얼
- 숫자 1을 걸려면 총 2초가 필요하다. 1보다 큰 수를 거는데 걸리는 시간은 이보다 더 걸리며,
  한 칸 옆에 있는 숫자를 걸기 위해선 1초씩 더 걸린다.
  상근이의 할머니는 전화 번호를 각 숫자에 해당하는 문자로 외운다.
  즉, 어떤 단어를 걸 때, 각 알파벳에 해당하는 숫자를 걸면 된다.
  예를 들어, UNUCIC는 868242와 같다.

주요 메서드 및 알고리즘
- 기존 if-else 구조를 배열 기반 매핑으로 리팩토링
- `'A'~'Z' ASCII` 차이를 활용한 인덱스 변환
- 코드 중복 제거 및 가독성 향
- 시간복잡도: O(n) / 공간복잡도: O(1)
*/
int main(void){
    char S[16];
    scanf("%s", S);

    // 다이얼 걸리는 시간 매핑 (A=0 인덱스부터)
    int dial[26] = {
        3, 3, 3,    // A,B,C
        4, 4, 4,    // D,E,F
        5, 5, 5,    // G,H,I
        6, 6, 6,    // J,K,L
        7, 7, 7,    // M,N,O
        8, 8, 8, 8, // P,Q,R,S
        9, 9, 9,    // T,U,V
        10, 10, 10, 10 // W,X,Y,Z
    };

    int cnt = 0;
    for(int i =0; i < S[i] != '\0'; i++){
        cnt += dial[S[i] - 'A'];
    }

    printf("%d\n",cnt);

    return 0;
}